
public class marks {

	public static void main(String[] args) {
		int n = 82;
		if (n >= 90) {
			System.out.println("A");
		} else if (n >= 80 && n < 90) {
			System.out.println("B");
		} else if (n >= 70 && n < 80) {
			System.out.println("C");
		} else if (n >= 60 && n < 70) {
			System.out.println("D");
		} else if (n >= 50 && n < 60) {
			System.out.println("E");
		}

	}

}
